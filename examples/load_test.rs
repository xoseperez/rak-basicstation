//! Load test utility for rak-basicstation.
//!
//! Simulates both sides simultaneously: a fake backend (Concentratord ZMQ or Semtech UDP)
//! injecting uplinks, and a fake LNS (WebSocket server) receiving them and optionally
//! sending downlinks back. Measures throughput, latency, and message loss.
//!
//! ```text
//! [Load Tester]                [rak-basicstation]              [Load Tester]
//!  Fake Backend  ──uplinks──►  Backend ──► LNS client  ──►   Fake LNS Server
//!  (ZMQ or UDP)  ◄──downlinks──         ◄── WebSocket ◄──   (WebSocket server)
//! ```
//!
//! Usage:
//!   cargo run --example load_test -- --rate 10 --duration 30
//!
//! Generate a matching rak-basicstation config:
//!   cargo run --example load_test -- --generate-config > /tmp/lt.toml
//!   cargo run -- -c /tmp/lt.toml

use std::collections::HashMap;
use std::sync::atomic::{AtomicBool, AtomicU64, Ordering};
use std::sync::{Arc, Mutex};
use std::time::{Duration, Instant};

use chirpstack_api::{gw, prost::Message};
use clap::Parser;
use futures_util::{SinkExt, StreamExt};
use tokio::net::{TcpListener, UdpSocket};

const DEV_ADDR: u32 = 0x2601_1234;
const EU868_FREQS: [u32; 8] = [
    868_100_000,
    868_300_000,
    868_500_000,
    867_100_000,
    867_300_000,
    867_500_000,
    867_700_000,
    867_900_000,
];

#[derive(Parser, Clone)]
#[command(name = "load_test", about = "Load test utility for rak-basicstation")]
struct Args {
    /// Uplinks per second
    #[arg(long, default_value = "10")]
    rate: u64,

    /// Test duration in seconds
    #[arg(long, default_value = "60")]
    duration: u64,

    /// Backend type: concentratord or semtech_udp
    #[arg(long, default_value = "concentratord")]
    backend: String,

    /// Fake LNS WebSocket listen address
    #[arg(long, default_value = "0.0.0.0:8887")]
    lns_bind: String,

    /// 16-char hex gateway EUI
    #[arg(long, default_value = "0016c001f156d7e5")]
    gateway_id: String,

    /// Fraction of uplinks triggering a downlink response
    #[arg(long, default_value = "0.1")]
    downlink_ratio: f64,

    /// ZMQ PUB socket bind address (concentratord backend)
    #[arg(long, default_value = "ipc:///tmp/lt_event")]
    zmq_event_bind: String,

    /// ZMQ REP socket bind address (concentratord backend)
    #[arg(long, default_value = "ipc:///tmp/lt_command")]
    zmq_command_bind: String,

    /// Semtech UDP target address
    #[arg(long, default_value = "127.0.0.1:1700")]
    udp_target: String,

    /// Print a matching rak-basicstation TOML config and exit
    #[arg(long)]
    generate_config: bool,
}

struct SharedState {
    metrics: Metrics,
    tracker: UplinkTracker,
    shutdown: AtomicBool,
    args: Args,
    ready: AtomicBool,
}

struct Metrics {
    uplinks_sent: AtomicU64,
    uplinks_received: AtomicU64,
    downlinks_sent: AtomicU64,
    downlinks_received: AtomicU64,
    latencies: Mutex<Vec<Duration>>,
    test_start: Mutex<Option<Instant>>,
}

struct UplinkTracker {
    timestamps: Mutex<HashMap<u32, Instant>>,
}

fn build_uplink_payload(seq: u32, dev_addr: u32) -> Vec<u8> {
    let mut payload = Vec::with_capacity(17);
    payload.push(0x40); // MHdr: unconfirmed data up
    payload.extend_from_slice(&dev_addr.to_le_bytes()); // DevAddr (4 bytes LE)
    payload.push(0x00); // FCtrl
    payload.extend_from_slice(&(seq as u16).to_le_bytes()); // FCnt (2 bytes LE)
    payload.push(0x01); // FPort = 1
    payload.extend_from_slice(&seq.to_be_bytes()); // FRMPayload = seq (4 bytes BE)
    payload.extend_from_slice(&[0x00; 4]); // MIC (dummy)
    payload
}

fn generate_config(args: &Args) {
    let backend = if args.backend == "semtech_udp" {
        "semtech_udp"
    } else {
        "concentratord"
    };

    let lns_host = if args.lns_bind.starts_with("0.0.0.0") {
        args.lns_bind.replacen("0.0.0.0", "127.0.0.1", 1)
    } else {
        args.lns_bind.clone()
    };

    println!(
        r#"# Auto-generated config for use with the load test utility.
# Generated by: cargo run --example load_test -- --generate-config

[logging]
  level = "warn"

[backend]
  enabled = "{backend}"
  gateway_id = "{gateway_id}"

  [backend.concentratord]
    event_url = "{event_url}"
    command_url = "{command_url}"

  [backend.semtech_udp]
    bind = "{udp_bind}"

[lns]
  server = "ws://{lns_host}"
  reconnect_interval = "1s"

[cups]
  enabled = false
"#,
        backend = backend,
        gateway_id = args.gateway_id,
        event_url = args.zmq_event_bind,
        command_url = args.zmq_command_bind,
        udp_bind = args.udp_target,
        lns_host = lns_host,
    );
}

