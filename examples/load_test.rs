async fn lns_server_task(state: Arc<SharedState>) {
    let listener = TcpListener::bind(&state.args.lns_bind)
        .await
        .unwrap_or_else(|e| panic!("Failed to bind LNS on {}: {}", state.args.lns_bind, e));
    println!("Fake LNS listening on {}", state.args.lns_bind);

    loop {
        if state.shutdown.load(Ordering::Relaxed) {
            break;
        }

        let (stream, addr) = match listener.accept().await {
            Ok(v) => v,
            Err(e) => {
                eprintln!("LNS accept error: {}", e);
                continue;
            }
        };

        let state = state.clone();
        tokio::spawn(async move {
            if let Err(e) = lns_connection_handler(stream, addr, state).await {
                eprintln!("LNS connection error ({}): {}", addr, e);
            }
        });
    }
}

async fn lns_connection_handler(
    stream: tokio::net::TcpStream,
    addr: std::net::SocketAddr,
    state: Arc<SharedState>,
) -> anyhow::Result<()> {
    let ws_stream = tokio_tungstenite::accept_async(stream).await?;
    let (mut ws_write, mut ws_read) = ws_stream.split();

    // Read first message to detect connection type.
    let first_msg = ws_read
        .next()
        .await
        .ok_or_else(|| anyhow::anyhow!("No message received"))??;

    let text = match first_msg {
        tokio_tungstenite::tungstenite::Message::Text(t) => t.to_string(),
        _ => return Ok(()),
    };

    let parsed: serde_json::Value = serde_json::from_str(&text)?;

    // Discovery request: has "router" key, no "msgtype".
    if parsed.get("router").is_some() && parsed.get("msgtype").is_none() {
        let lns_host = if state.args.lns_bind.starts_with("0.0.0.0") {
            state.args.lns_bind.replacen("0.0.0.0", "127.0.0.1", 1)
        } else {
            state.args.lns_bind.clone()
        };

        let resp = serde_json::json!({
            "router": parsed["router"],
            "muxs": parsed["router"],
            "uri": format!("ws://{}", lns_host),
        });
        ws_write
            .send(tokio_tungstenite::tungstenite::Message::Text(
                resp.to_string().into(),
            ))
            .await?;
        println!("[LNS] Discovery from {} → {}", addr, lns_host);
        return Ok(());
    }

    // Main connection: first message should be "version".
    if let Some(msgtype) = parsed.get("msgtype").and_then(|v| v.as_str())
        && msgtype == "version"
    {
        let firmware = parsed
            .get("firmware")
            .and_then(|v| v.as_str())
            .unwrap_or("unknown");
        println!("[LNS] Main connection from {}, firmware: {}", addr, firmware);

        // Send router_config.
        let rc = router_config_json();
        ws_write
            .send(tokio_tungstenite::tungstenite::Message::Text(rc.into()))
            .await?;
        println!("[LNS] Sent router_config to {}", addr);

        // Signal readiness.
        state.ready.store(true, Ordering::Release);

        // Enter message loop.
        lns_message_loop(&mut ws_write, &mut ws_read, &state).await?;
    }

    Ok(())
}

async fn lns_message_loop(
    ws_write: &mut futures_util::stream::SplitSink<
        tokio_tungstenite::WebSocketStream<tokio::net::TcpStream>,
        tokio_tungstenite::tungstenite::Message,
    >,
    ws_read: &mut futures_util::stream::SplitStream<
        tokio_tungstenite::WebSocketStream<tokio::net::TcpStream>,
    >,
    state: &Arc<SharedState>,
) -> anyhow::Result<()> {
    let mut uplink_count: u64 = 0;
    let mut downlink_diid: i64 = 0;
    let downlink_interval = if state.args.downlink_ratio > 0.0 {
        (1.0 / state.args.downlink_ratio).round() as u64
    } else {
        0
    };

    while let Some(msg) = ws_read.next().await {
        if state.shutdown.load(Ordering::Relaxed) {
            break;
        }

        let msg = msg?;
        let text = match msg {
            tokio_tungstenite::tungstenite::Message::Text(t) => t.to_string(),
            tokio_tungstenite::tungstenite::Message::Close(_) => break,
            _ => continue,
        };

        let parsed: serde_json::Value = match serde_json::from_str(&text) {
            Ok(v) => v,
            Err(_) => continue,
        };

        let msgtype = match parsed.get("msgtype").and_then(|v| v.as_str()) {
            Some(t) => t,
            None => continue,
        };

        match msgtype {
            "updf" => {
                uplink_count += 1;
                state
                    .metrics
                    .uplinks_received
                    .fetch_add(1, Ordering::Relaxed);

                // Extract seq from FRMPayload (4 bytes BE hex).
                if let Some(frm_hex) = parsed.get("FRMPayload").and_then(|v| v.as_str())
                    && let Ok(frm_bytes) = hex::decode(frm_hex)
                    && frm_bytes.len() == 4
                {
                    let seq = u32::from_be_bytes([
                        frm_bytes[0],
                        frm_bytes[1],
                        frm_bytes[2],
                        frm_bytes[3],
                    ]);
                    let mut tracker = state.tracker.timestamps.lock().unwrap();
                    if let Some(sent_at) = tracker.remove(&seq) {
                        let latency = sent_at.elapsed();
                        state.metrics.latencies.lock().unwrap().push(latency);
                    }
                }

                // Send downlink if configured.
                if downlink_interval > 0 && uplink_count % downlink_interval == 0 {
                    let xtime = parsed
                        .get("upinfo")
                        .and_then(|u| u.get("xtime"))
                        .and_then(|v| v.as_i64())
                        .unwrap_or(0);
                    let rctx = parsed
                        .get("upinfo")
                        .and_then(|u| u.get("rctx"))
                        .and_then(|v| v.as_i64())
                        .unwrap_or(0);
                    let freq = parsed.get("Freq").and_then(|v| v.as_u64()).unwrap_or(868_100_000);

                    downlink_diid += 1;
                    let dev_addr_le = DEV_ADDR.to_le_bytes();
                    let pdu = format!(
                        "60{}0000010001020304aabbccdd",
                        hex::encode(dev_addr_le)
                    );

                    let dnmsg = serde_json::json!({
                        "msgtype": "dnmsg",
                        "DevEui": "00-00-00-00-00-00-00-01",
                        "dC": 0,
                        "diid": downlink_diid,
                        "pdu": pdu,
                        "RxDelay": 1,
                        "RX1DR": 5,
                        "RX1Freq": freq,
                        "RX2DR": 0,
                        "RX2Freq": 869525000,
                        "xtime": xtime,
                        "rctx": rctx,
                        "priority": 0,
                    });

                    if ws_write
                        .send(tokio_tungstenite::tungstenite::Message::Text(
                            dnmsg.to_string().into(),
                        ))
                        .await
                        .is_ok()
                    {
                        state
                            .metrics
                            .downlinks_sent
                            .fetch_add(1, Ordering::Relaxed);
                    }
                }
            }
            "dntxed" => {
                // Downlink TX confirmation — just count it.
            }
            "timesync" => {
                // Respond with a timesync response.
                let txtime = parsed.get("txtime").and_then(|v| v.as_i64()).unwrap_or(0);
                let resp = serde_json::json!({
                    "msgtype": "timesync",
                    "txtime": txtime,
                });
                let _ = ws_write
                    .send(tokio_tungstenite::tungstenite::Message::Text(
                        resp.to_string().into(),
                    ))
                    .await;
            }
            _ => {}
        }
    }

    Ok(())
}

