// Progress and reporting
// ---------------------------------------------------------------------------

async fn progress_reporter(state: Arc<SharedState>) {
    let mut interval = tokio::time::interval(Duration::from_secs(5));
    interval.tick().await; // skip first immediate tick

    loop {
        interval.tick().await;

        if state.shutdown.load(Ordering::Relaxed) {
            break;
        }

        let elapsed = {
            let start = state.metrics.test_start.lock().unwrap();
            match *start {
                Some(s) => s.elapsed().as_secs(),
                None => continue,
            }
        };

        let sent = state.metrics.uplinks_sent.load(Ordering::Relaxed);
        let received = state.metrics.uplinks_received.load(Ordering::Relaxed);
        let dl_sent = state.metrics.downlinks_sent.load(Ordering::Relaxed);
        let dl_recv = state.metrics.downlinks_received.load(Ordering::Relaxed);

        let pct = if sent > 0 {
            (received as f64 / sent as f64) * 100.0
        } else {
            0.0
        };
        let throughput = if elapsed > 0 {
            received as f64 / elapsed as f64
        } else {
            0.0
        };

        println!(
            "[{:>3}s] uplinks: {}/{} ({:.1}%) | downlinks: {}/{} | throughput: {:.1} msg/s",
            elapsed, received, sent, pct, dl_recv, dl_sent, throughput,
        );
    }
}

fn print_report(state: &SharedState) {
    let elapsed = {
        let start = state.metrics.test_start.lock().unwrap();
        match *start {
            Some(s) => s.elapsed().as_secs_f64(),
            None => state.args.duration as f64,
        }
    };

    let sent = state.metrics.uplinks_sent.load(Ordering::Relaxed);
    let received = state.metrics.uplinks_received.load(Ordering::Relaxed);
    let lost = sent.saturating_sub(received);
    let loss_pct = if sent > 0 {
        (lost as f64 / sent as f64) * 100.0
    } else {
        0.0
    };
    let throughput = if elapsed > 0.0 {
        received as f64 / elapsed
    } else {
        0.0
    };

    let dl_sent = state.metrics.downlinks_sent.load(Ordering::Relaxed);
    let dl_recv = state.metrics.downlinks_received.load(Ordering::Relaxed);

    let mut latencies = state.metrics.latencies.lock().unwrap();
    latencies.sort();
    let samples = latencies.len();

    let (p50, p95, p99, max) = if !latencies.is_empty() {
        let p = |pct: f64| {
            let idx = ((pct / 100.0) * (latencies.len() - 1) as f64).round() as usize;
            latencies[idx]
        };
        (p(50.0), p(95.0), p(99.0), *latencies.last().unwrap())
    } else {
        (Duration::ZERO, Duration::ZERO, Duration::ZERO, Duration::ZERO)
    };

    println!();
    println!("=== Load Test Report ===");
    println!("Duration:            {:.1}s", elapsed);
    println!("Backend:             {}", state.args.backend);
    println!("Rate (configured):   {} msg/s", state.args.rate);
    println!();
    println!("--- Uplinks ---");
    println!("  Sent:              {}", sent);
    println!("  Received:          {}", received);
    println!("  Lost:              {} ({:.2}%)", lost, loss_pct);
    println!("  Throughput:        {:.2} msg/s", throughput);
    println!();
    println!("--- Downlinks ---");
    println!("  Sent (by LNS):    {}", dl_sent);
    println!("  Received:          {}", dl_recv);
    println!();
    println!("--- Latency (end-to-end uplink) ---");
    println!("  Samples:           {}", samples);
    println!("  p50:               {:.2} ms", p50.as_secs_f64() * 1000.0);
    println!("  p95:               {:.2} ms", p95.as_secs_f64() * 1000.0);
    println!("  p99:               {:.2} ms", p99.as_secs_f64() * 1000.0);
    println!("  Max:               {:.2} ms", max.as_secs_f64() * 1000.0);
    println!("========================");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

#[tokio::main]
async fn main() {
    let args = Args::parse();

    if args.generate_config {
        generate_config(&args);
        return;
    }

    println!("=== rak-basicstation Load Test ===");
    println!("Backend:         {}", args.backend);
    println!("Rate:            {} msg/s", args.rate);
    println!("Duration:        {}s", args.duration);
    println!("LNS bind:        {}", args.lns_bind);
    println!("Gateway ID:      {}", args.gateway_id);
    println!("Downlink ratio:  {}", args.downlink_ratio);

    let state = Arc::new(SharedState {
        metrics: Metrics {
            uplinks_sent: AtomicU64::new(0),
            uplinks_received: AtomicU64::new(0),
            downlinks_sent: AtomicU64::new(0),
            downlinks_received: AtomicU64::new(0),
            latencies: Mutex::new(Vec::new()),
            test_start: Mutex::new(None),
        },
        tracker: UplinkTracker {
            timestamps: Mutex::new(HashMap::new()),
        },
        shutdown: AtomicBool::new(false),
        ready: AtomicBool::new(false),
        args: args.clone(),
    });

    // Start fake LNS server.
    let lns_state = state.clone();
    tokio::spawn(async move {
        lns_server_task(lns_state).await;
    });

    // Start progress reporter.
    let progress_state = state.clone();
    tokio::spawn(async move {
        progress_reporter(progress_state).await;
    });

    // Start backend.
    match args.backend.as_str() {
        "concentratord" => {
            println!("ZMQ event bind:  {}", args.zmq_event_bind);
            println!("ZMQ command bind: {}", args.zmq_command_bind);

            // REP handler (blocking, std thread).
            let rep_state = state.clone();
            std::thread::spawn(move || zmq_rep_handler(rep_state));

            // PUB publisher (blocking, std thread).
            let pub_state = state.clone();
            std::thread::spawn(move || zmq_pub_publisher(pub_state));
        }
        "semtech_udp" => {
            println!("UDP target:      {}", args.udp_target);

            // Bind on port 0 for sending; the receiver will get responses on the same socket.
            let socket = Arc::new(
                UdpSocket::bind("0.0.0.0:0")
                    .await
                    .expect("Failed to bind UDP socket"),
            );
            println!(
                "UDP local addr:  {}",
                socket.local_addr().unwrap()
            );

            let sender_state = state.clone();
            let sender_socket = socket.clone();
            tokio::spawn(async move {
                udp_sender_task(sender_state, sender_socket).await;
            });

            let receiver_state = state.clone();
            let receiver_socket = socket.clone();
            tokio::spawn(async move {
                udp_receiver_task(receiver_state, receiver_socket).await;
            });
        }
        other => {
            eprintln!("Unknown backend: {}. Use 'concentratord' or 'semtech_udp'.", other);
            std::process::exit(1);
        }
    }

    println!();
    println!("Waiting for rak-basicstation to connect...");
    println!("Start it with a matching config, e.g.:");
    println!("  cargo run --example load_test -- --generate-config > /tmp/lt.toml");
    println!("  cargo run -- -c /tmp/lt.toml");
    println!();

    // Wait for the test duration plus a grace period for readiness.
    // The actual timing is handled by the publisher threads.
    let total_wait = Duration::from_secs(args.duration + 300); // 5 min max wait for connection
    let start = Instant::now();

    loop {
        tokio::time::sleep(Duration::from_millis(500)).await;

        // If the test start has been recorded and duration has elapsed, stop.
        let test_started = state.metrics.test_start.lock().unwrap().is_some();
        if test_started {
            let test_start = state.metrics.test_start.lock().unwrap().unwrap();
            if test_start.elapsed() >= Duration::from_secs(args.duration) {
                // Give a short grace period for in-flight messages.
                tokio::time::sleep(Duration::from_secs(2)).await;
                break;
            }
        }

        if start.elapsed() >= total_wait {
            eprintln!("Timeout waiting for test to complete.");
            break;
        }
    }

    state.shutdown.store(true, Ordering::Release);

    // Brief pause for threads to clean up.
    tokio::time::sleep(Duration::from_millis(500)).await;

    print_report(&state);
}
