async fn udp_sender_task(state: Arc<SharedState>, socket: Arc<UdpSocket>) {
    // Wait for readiness.
    while !state.ready.load(Ordering::Acquire) {
        if state.shutdown.load(Ordering::Relaxed) {
            return;
        }
        tokio::time::sleep(Duration::from_millis(100)).await;
    }

    // Brief delay for the gateway to register via PULL_DATA.
    tokio::time::sleep(Duration::from_secs(2)).await;
    println!("Starting uplink generation (semtech_udp)...");

    // Record test start time.
    {
        let mut start = state.metrics.test_start.lock().unwrap();
        *start = Some(Instant::now());
    }

    let interval = if state.args.rate > 0 {
        Duration::from_secs_f64(1.0 / state.args.rate as f64)
    } else {
        Duration::from_secs(1)
    };

    let mut uplink_interval = tokio::time::interval(interval);
    let mut pull_interval = tokio::time::interval(Duration::from_secs(5));
    let gateway_id_bytes = hex::decode(&state.args.gateway_id).unwrap_or_else(|_| vec![0u8; 8]);
    let target: std::net::SocketAddr = state
        .args
        .udp_target
        .parse()
        .expect("Invalid UDP target address");

    let mut seq: u32 = 0;
    let mut token: u16 = 0;
    let test_end = Instant::now() + Duration::from_secs(state.args.duration);

    loop {
        if state.shutdown.load(Ordering::Relaxed) || Instant::now() >= test_end {
            break;
        }

        tokio::select! {
            _ = uplink_interval.tick() => {
                seq += 1;
                token = token.wrapping_add(1);
                let freq = EU868_FREQS[(seq as usize) % EU868_FREQS.len()];
                let payload = build_uplink_payload(seq, DEV_ADDR);

                let rxpk = serde_json::json!({
                    "rxpk": [{
                        "tmst": seq * 1000,
                        "freq": freq as f64 / 1_000_000.0,
                        "chan": (seq as usize % 8) as u32,
                        "rfch": 0,
                        "stat": 1,
                        "modu": "LORA",
                        "datr": "SF7BW125",
                        "codr": "4/5",
                        "rssi": -60,
                        "lsnr": 10.0,
                        "size": payload.len(),
                        "data": base64::Engine::encode(&base64::engine::general_purpose::STANDARD, &payload),
                    }]
                });

                let json_bytes = serde_json::to_vec(&rxpk).unwrap();
                let mut packet = Vec::with_capacity(12 + json_bytes.len());
                packet.push(0x02); // protocol version
                packet.extend_from_slice(&token.to_le_bytes());
                packet.push(0x00); // PUSH_DATA
                let gw_id: [u8; 8] = gateway_id_bytes[..8].try_into().unwrap_or([0u8; 8]);
                packet.extend_from_slice(&gw_id);
                packet.extend_from_slice(&json_bytes);

                if socket.send_to(&packet, target).await.is_ok() {
                    state.metrics.uplinks_sent.fetch_add(1, Ordering::Relaxed);
                    state.tracker.timestamps.lock().unwrap().insert(seq, Instant::now());
                }
            }
            _ = pull_interval.tick() => {
                // Send PULL_DATA to register the gateway.
                token = token.wrapping_add(1);
                let mut packet = Vec::with_capacity(12);
                packet.push(0x02); // protocol version
                packet.extend_from_slice(&token.to_le_bytes());
                packet.push(0x02); // PULL_DATA
                let gw_id: [u8; 8] = gateway_id_bytes[..8].try_into().unwrap_or([0u8; 8]);
                packet.extend_from_slice(&gw_id);
                let _ = socket.send_to(&packet, target).await;
            }
        }
    }
}

async fn udp_receiver_task(state: Arc<SharedState>, socket: Arc<UdpSocket>) {
    let mut buf = [0u8; 65535];

    loop {
        if state.shutdown.load(Ordering::Relaxed) {
            break;
        }

        let result = tokio::time::timeout(Duration::from_secs(1), socket.recv_from(&mut buf)).await;

        let (size, remote) = match result {
            Ok(Ok(v)) => v,
            Ok(Err(_)) => continue,
            Err(_) => continue, // timeout
        };

        if size < 4 {
            continue;
        }

        match buf[3] {
            0x01 => {} // PUSH_ACK — ignore
            0x04 => {} // PULL_ACK — ignore
            0x03 => {
                // PULL_RESP — downlink received
                state
                    .metrics
                    .downlinks_received
                    .fetch_add(1, Ordering::Relaxed);

                // Send TX_ACK.
                let token = [buf[1], buf[2]];
                let gw_id = hex::decode(&state.args.gateway_id).unwrap_or_else(|_| vec![0u8; 8]);

                let ack_payload = serde_json::json!({"txpk_ack": {"error": "NONE"}});
                let ack_json = serde_json::to_vec(&ack_payload).unwrap();

                let mut ack = Vec::with_capacity(12 + ack_json.len());
                ack.push(0x02); // protocol version
                ack.extend_from_slice(&token);
                ack.push(0x05); // TX_ACK
                let gw_bytes: [u8; 8] = gw_id[..8].try_into().unwrap_or([0u8; 8]);
                ack.extend_from_slice(&gw_bytes);
                ack.extend_from_slice(&ack_json);
                let _ = socket.send_to(&ack, remote).await;
            }
            _ => {}
        }
    }
}

