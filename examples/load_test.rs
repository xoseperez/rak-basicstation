fn zmq_rep_handler(state: Arc<SharedState>) {
    let ctx = zmq::Context::new();
    let cmd_sock = ctx.socket(zmq::REP).expect("REP socket");

    // Clean up stale IPC socket.
    if let Some(path) = state.args.zmq_command_bind.strip_prefix("ipc://") {
        let _ = std::fs::remove_file(path);
    }

    cmd_sock
        .bind(&state.args.zmq_command_bind)
        .unwrap_or_else(|e| panic!("ZMQ REP bind failed ({}): {}", state.args.zmq_command_bind, e));
    println!("ZMQ REP bound on {}", state.args.zmq_command_bind);

    loop {
        if state.shutdown.load(Ordering::Relaxed) {
            break;
        }

        // Use a polling approach so we can check shutdown.
        if cmd_sock.poll(zmq::POLLIN, 500).unwrap_or(0) == 0 {
            continue;
        }

        let msg = match cmd_sock.recv_bytes(zmq::DONTWAIT) {
            Ok(m) => m,
            Err(_) => continue,
        };

        let cmd = match gw::Command::decode(msg.as_slice()) {
            Ok(c) => c,
            Err(e) => {
                eprintln!("ZMQ cmd decode error: {}", e);
                let _ = cmd_sock.send(&b""[..], 0);
                continue;
            }
        };

        match cmd.command {
            Some(gw::command::Command::GetGatewayId(_)) => {
                let resp = gw::GetGatewayIdResponse {
                    gateway_id: state.args.gateway_id.clone(),
                };
                let _ = cmd_sock.send(resp.encode_to_vec(), 0);
            }
            Some(gw::command::Command::SetGatewayConfiguration(cfg)) => {
                println!(
                    "[ZMQ] SetGatewayConfiguration, channels: {}",
                    cfg.channels.len()
                );
                let _ = cmd_sock.send(&b""[..], 0);
            }
            Some(gw::command::Command::SendDownlinkFrame(dl)) => {
                state
                    .metrics
                    .downlinks_received
                    .fetch_add(1, Ordering::Relaxed);
                let ack = gw::DownlinkTxAck {
                    gateway_id: state.args.gateway_id.clone(),
                    downlink_id: dl.downlink_id,
                    items: vec![gw::DownlinkTxAckItem {
                        status: gw::TxAckStatus::Ok.into(),
                    }],
                    ..Default::default()
                };
                let _ = cmd_sock.send(ack.encode_to_vec(), 0);
            }
            _ => {
                let _ = cmd_sock.send(&b""[..], 0);
            }
        }
    }
}

fn zmq_pub_publisher(state: Arc<SharedState>) {
    let ctx = zmq::Context::new();
    let pub_sock = ctx.socket(zmq::PUB).expect("PUB socket");

    // Clean up stale IPC socket.
    if let Some(path) = state.args.zmq_event_bind.strip_prefix("ipc://") {
        let _ = std::fs::remove_file(path);
    }

    pub_sock
        .bind(&state.args.zmq_event_bind)
        .unwrap_or_else(|e| panic!("ZMQ PUB bind failed ({}): {}", state.args.zmq_event_bind, e));
    println!("ZMQ PUB bound on {}", state.args.zmq_event_bind);

    // Wait for readiness (rak-basicstation connected to LNS and received router_config).
    println!("Waiting for rak-basicstation to connect...");
    while !state.ready.load(Ordering::Acquire) {
        if state.shutdown.load(Ordering::Relaxed) {
            return;
        }
        std::thread::sleep(Duration::from_millis(100));
    }

    // Give concentratord subscriber time to connect.
    std::thread::sleep(Duration::from_secs(2));
    println!("Starting uplink generation (concentratord)...");

    // Record test start time.
    {
        let mut start = state.metrics.test_start.lock().unwrap();
        *start = Some(Instant::now());
    }

    let interval = if state.args.rate > 0 {
        Duration::from_secs_f64(1.0 / state.args.rate as f64)
    } else {
        Duration::from_secs(1)
    };

    let mut seq: u32 = 0;
    let test_end = Instant::now() + Duration::from_secs(state.args.duration);

    while Instant::now() < test_end {
        if state.shutdown.load(Ordering::Relaxed) {
            break;
        }

        seq += 1;
        let freq = EU868_FREQS[(seq as usize) % EU868_FREQS.len()];
        let payload = build_uplink_payload(seq, DEV_ADDR);
        let context_val = (seq * 1000).to_be_bytes();

        let uplink = gw::UplinkFrame {
            phy_payload: payload,
            tx_info: Some(gw::UplinkTxInfo {
                frequency: freq,
                modulation: Some(gw::Modulation {
                    parameters: Some(gw::modulation::Parameters::Lora(
                        gw::LoraModulationInfo {
                            bandwidth: 125_000,
                            spreading_factor: 7,
                            code_rate: gw::CodeRate::Cr45.into(),
                            ..Default::default()
                        },
                    )),
                }),
            }),
            rx_info: Some(gw::UplinkRxInfo {
                uplink_id: seq,
                context: context_val.to_vec(),
                rssi: -60,
                snr: 10.0,
                crc_status: gw::CrcStatus::CrcOk.into(),
                ..Default::default()
            }),
            ..Default::default()
        };

        let event = gw::Event {
            event: Some(gw::event::Event::UplinkFrame(uplink)),
        };

        if pub_sock.send(event.encode_to_vec(), 0).is_ok() {
            state
                .metrics
                .uplinks_sent
                .fetch_add(1, Ordering::Relaxed);
            state
                .tracker
                .timestamps
                .lock()
                .unwrap()
                .insert(seq, Instant::now());
        }

        std::thread::sleep(interval);
    }
}

